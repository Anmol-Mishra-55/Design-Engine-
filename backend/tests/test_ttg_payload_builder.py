class TestOutputStructure:
    def test_returns_ttg_execute_payload_instance(self):
        assert isinstance(build(), TTGExecutePayload)

    def test_execution_id_propagated(self):
        p = build(execution_id="exec_custom_99")
        assert p.execution_id == "exec_custom_99"

    def test_execution_id_autogenerated_when_none(self):
        builder = TTGPayloadBuilder()
        p = builder.build(
            semantic_resolution=SEMANTIC["vehicle"],
            spec_json=SPEC_JSON,
            bucket_urls=BUCKET_URLS,
            trace_id="trace_001",
        )
        assert p.execution_id.startswith("exec_")

    def test_trace_id_propagated(self):
        p = build(trace_id="trace_custom_abc")
        assert p.trace_id == "trace_custom_abc"

    def test_spec_json_preserved_exactly(self):
        p = build()
        assert p.spec_json == SPEC_JSON

    def test_bucket_urls_preserved(self):
        p = build()
        assert p.bucket_urls == BUCKET_URLS

    def test_asset_refs_defaults_to_empty_list(self):
        p = build()
        assert p.asset_refs == []

    def test_asset_refs_propagated_when_provided(self):
        refs = ["https://bhiv-bucket.onrender.com/bucket/artifact/ref001"]
        p = build(asset_refs=refs)
        assert p.asset_refs == refs

    def test_to_dict_contains_all_six_required_keys(self):
        d = build().to_dict()
        for key in ("execution_id", "trace_id", "executionSchema", "spec_json", "asset_refs", "bucket_urls"):
            assert key in d, f"Missing key: {key}"

    def test_to_full_dict_contains_meta_block(self):
        fd = build().to_full_dict()
        assert "_meta" in fd
        assert "domain" in fd["_meta"]
        assert "schema_type" in fd["_meta"]
        assert "output_formats" in fd["_meta"]

    def test_domain_field_populated(self):
        assert build("vehicle").domain == "vehicle"
        assert build("architecture").domain == "architecture"

    def test_schema_type_matches_domain_table(self):
        for domain in ("vehicle", "architecture", "object", "environment", "gameplay"):
            p = build(domain)
            assert p.schema_type == _DOMAIN_SCHEMA_TYPE[domain]

    def test_output_formats_match_domain_table(self):
        for domain in ("vehicle", "architecture", "object", "environment", "gameplay"):
            p = build(domain)
            assert p.output_formats == _DOMAIN_OUTPUT_FORMATS[domain]

    def test_multiple_bucket_urls_preserved(self):
        urls = [
            "https://bhiv-bucket.onrender.com/bucket/artifact/abc",
            "https://bhiv-bucket.onrender.com/bucket/artifact/def",
            "https://bhiv-bucket.onrender.com/bucket/artifact/ghi",
        ]
        p = build(bucket_urls=urls)
        assert p.bucket_urls == urls


