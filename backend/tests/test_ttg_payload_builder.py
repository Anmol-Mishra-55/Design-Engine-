def build(domain: str = "vehicle", **overrides) -> TTGExecutePayload:
    """Helper — build a payload for a given domain with optional overrides."""
    builder = TTGPayloadBuilder()
    kwargs = dict(
        semantic_resolution=SEMANTIC[domain],
        spec_json=SPEC_JSON,
        bucket_urls=BUCKET_URLS,
        trace_id="trace_test_001",
        execution_id="exec_test_001",
        metadata=METADATA,
    )
    kwargs.update(overrides)
    return builder.build(**kwargs)


